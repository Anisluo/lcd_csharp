        //色度采集
        public override IData MeasureLxy()
        {
            if (!tryMeasure()) { return null; }
            if (currIntegratetime < 1) { wrapper.setScansToAverage(0, 8); }//设置平均次数为4 
            else { wrapper.setScansToAverage(0, 1); }//设置平均次数为1次
            SpecrumIntensity = null;
            while (SpecrumIntensity == null)
            {
                SpecrumIntensity = wrapper.getSpectrum(0);//读取当前光强数据
            }
            Spectrumdic.Clear();//清空数据
            for (int i = 0; i < 401; i++)
            {
                //Spectrumdic.Add(380 + i, SpecrumIntensity[Spectrum2xyzCodf.wavepos[i]]);
                Spectrumdic.Add(380 + i, SpecrumIntensity[myxmlparse.spectrumAdjCoef.Keys[i]]);//添加光谱数据及对应光强数据
                //采集特定位置下的光谱强度
            }
            //计算X值
            X = 0.0;
            Y = 0.0;
            Z = 0.0;
            for (int i = 0; i < 401; i++)
            {
                //Spectrumdic[i + 380] *= Spectrum2xyzCodf.calccoef[i];
                Spectrumdic[i + 380] *= myxmlparse.spectrumAdjCoef.Values[i];//光强数据做偏差校正
                //获取指定位置下的修正值
            }
            //波长范围从380~780
            for (int i = 0; i < 401; i++)
            {
                X += Spectrum2xyzCodf.xcoef[i] * Spectrumdic[i + 380];
                Y += Spectrum2xyzCodf.ycoef[i] * Spectrumdic[i + 380];
                Z += Spectrum2xyzCodf.zcoef[i] * Spectrumdic[i + 380];
            }
            IData Result = new IData();
            //caculate from the XYZ
            X /= (currIntegratetime * IntegrateTimeUnit);
            Y /= (currIntegratetime * IntegrateTimeUnit);
            Z /= (currIntegratetime * IntegrateTimeUnit);
            //X,Y,Z三刺激值/曝光时间
            //string str = "";
            double cx = (X / (X + Y + Z));
            double cy = (Y / (X + Y + Z));
            double L = (Y * lumianceCoef);
            double u = (4 * X / (X + 15 * Y + 3 * Z));
            double v = (9 * Y / (X + 15 * Y + 3 * Z));
            //FResult.Tc = 0;//Tc值统一设置为0
            double tempn = (cx - 0.332) / (0.185 - cy);
            double Tc = 437 * (Math.Pow(tempn, 3)) + 3601 * Math.Pow(tempn, 2) + 6831 * tempn + 5517;

            Result.L = L;
            Result.X = X;
            Result.Y = Y;
            Result.Z = Z;
            Result.Cx = cx;
            Result.Cy = cy;
            Result.u = u;
            Result.v = v;
            Result.Tc = Tc;
            return Result;
        }



