    public class MyXmlParse
    {
        //光标测量矫正参数
        public SortedList<int, double> spectrumAdjCoef = new SortedList<int, double>();
        public void InitXmlData()//初始化xml数据
        {
            string fileName = "SpectrumAdjust.xml";
            string configlFile = AppDomain.CurrentDomain.BaseDirectory + fileName;//获得当前文件名
            if (!System.IO.File.Exists(configlFile))
            {
                Project.WriteLog("光谱偏差矫正参数SpectrumAdjust.xml文件读取失败");
            }
            try
            {
                XmlDocument xd = new System.Xml.XmlDocument();
                xd.Load(configlFile);//载入xml文件
                XmlElement root = xd.DocumentElement;
                spectrumAdjCoef.Clear();//清空参数内容
                foreach (XmlElement item in root.ChildNodes.OfType<XmlElement>())
                {
                    string codeTxt = string.Empty;
                    string nameTxt = string.Empty;
                    var myindex = item.Attributes["index"];
                    var myvalue = item.Attributes["value"];

                    if (myvalue != null && myindex != null)
                    {
                        spectrumAdjCoef.Add(int.Parse(myindex.Value), double.Parse(myvalue.Value));//填充数据
                    }
                }
                if (spectrumAdjCoef.Count != 401)
                {
                    Project.WriteLog("SpectrumAdjust.xml文件中存在数据缺失");
                }
            }
            catch (Exception ex)
            {
                string log = "解析xml文件报错，" + ex.Message;
            }
        }

        public void ReadSeriesXml()
        {
            string fileName = "Series.xml";//读取Series文件
            string configlFile = AppDomain.CurrentDomain.BaseDirectory + fileName;//获得当前文件名
            if (!System.IO.File.Exists(configlFile))
            {
                Project.WriteLog("读取配置文件失败!");
            }
            try
            {
                XmlDocument xd = new System.Xml.XmlDocument();
                xd.Load(configlFile);//载入xml文件
                XmlElement root = xd.DocumentElement;
                spectrumAdjCoef.Clear();//清空参数内容
                foreach (XmlElement item in root.ChildNodes.OfType<XmlElement>())
                {
                    string codeTxt = string.Empty;
                    string nameTxt = string.Empty;
                    var myindex = item.Attributes["index"];
                    var myvalue = item.Attributes["value"];
                    if (myvalue != null && myindex != null)
                    {
                        spectrumAdjCoef.Add(int.Parse(myindex.Value), double.Parse(myvalue.Value));//填充数据
                    }
                }
                if (spectrumAdjCoef.Count != 401)
                {
                    Project.WriteLog("SpectrumAdjust.xml文件中存在数据缺失");
                }
            }
            catch (Exception ex)
            {
                string log = "解析xml文件报错，" + ex.Message;
            }
        }
