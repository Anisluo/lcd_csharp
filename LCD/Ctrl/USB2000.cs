        /// <summary>
        /// 功能：通过试测计算积分时间
        /// </summary>
        /// <returns></returns>
        private bool tryMeasure()
        {
            try
            {
                StartUpInstrument();
                //currIntegratetime = 50000;//厨师默认参数为50000
                currIntegratetime = 4;
                while (true)
                {
                    //检验是否处于急停状态中
                    //if (GlobalConfig.bQuickStop)
                    //    return false;
                    wrapper.setIntegrationTime(0, (int)(currIntegratetime * IntegrateTimeUnit));
                    SpecrumIntensity = wrapper.getSpectrum(0);//获取光强数据
                    double tempIntensity = SpecrumIntensity.Max();
                    if (currIntegratetime >= 120)
                    {
                        currIntegratetime = 120;
                        break;//超过2s之后退出试测环节
                    }
                    if (tempIntensity > 50000)
                    {
                        currIntegratetime /= 2;
                    }

                    else if (tempIntensity < 20000)
                    {
                        currIntegratetime *= 2;//当光强数小于
                    }
                    else
                    {
                        break;
                    }
                }
                return true;
            }
            catch (Exception ex)
            {
                VisionCore.Log.Fatal(ex);
                return false;
            }
        }

